#include "3-calc.h"

/**
 * op_add - function to add
 * @a: integer one
 * @b: integer two
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract
 * @a: integer one
 * @b: integer two
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply
 * @a: integer one
 * @b: integer two
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide
 * @a: integer one
 * @b: integer two
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to compute modulus
 * @a: integer one
 * @b: integer two
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
