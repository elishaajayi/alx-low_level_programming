#include "func.h"

/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - add two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - add two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - add two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - add two numbers
 * @a: first number
 * @b: second number
 * Return: result of operation
 */
int mod(int a, int b)
{
	return (a % b);
}

